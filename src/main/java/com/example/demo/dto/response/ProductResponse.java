import lombok.experimental.FieldDefaults;

import java.util.List;

@Data
@NoArgsConstructor
@AllArgsConstructor
@FieldDefaults(level = AccessLevel.PRIVATE)
public class ProductResponse {
    String id;
    String name;
    String description;
    double price;
    int stock;
    String category;
    String imageUrl;
    private List<ProductSizeResponse> sizes;
}
